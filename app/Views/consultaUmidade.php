<script>
    $(document).ready(function() {
        $('#resultado').hide();
        $('#btnConsultHumidity').click(function(e) {
            e.preventDefault();

            var formData = $('#dataAlerts').serializeArray();
            console.log(formData);

            $.ajax({
                url: 'http://localhost/consulta-umidade/public/consultaUmidade/verificaUmidade',
                type: 'POST',
                data: formData,
                success: function(data) {
                    $('#resultado').html(data.message).show();
                },
                error: function(xhr, status, error) {
                    $('#resultado').html('Erro ao consultar a umidade.');
                }
            });
        });
    });
